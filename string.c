/********************************************************************************/
/*		memcpy																	*/
/********************************************************************************/
char memcpy(char *dst, char *src, int size)
{
	int i;
	
	for(i=0; i<size; i+=1){
		dst[i] = src[i];
	}
	return 0;
}
/********************************************************************************/
/*		memset																	*/
/********************************************************************************/
char memset(char *dst, char data, int size)
{
	int i;
	
	for(i=0; i<size; i+=1){
		dst[i] = data;
	}
	return 0;
}
/********************************************************************************/
/*		memcmp																	*/
/********************************************************************************/
char memcmp(char *dst, char *src, int size)
{
	int i;
	char tmp;
	
	for(i=0; i<size; i+=1){
		if(tmp = dst[i]-src[i])
			return tmp;
	}
	return 0;
}
